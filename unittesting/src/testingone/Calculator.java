package testingone;

public class Calculator {
    /**
     *
     * @param num1
     * @param num2
     * @return
     */
    public static int add(int num1, int num2) {
        return num1 + num2;

    }

    /**
     *
     * @param num1 // holds numbers
     * @param num2
     * @return
     */
    public static int subtract(int num1, int num2) {
        return num1 - num2;


    }

    /**
     *
     * @param num1
     * @param num2
     * @return
     */
    public static  int multiply(int num1, int num2) {
        return num1 * num2;


    }

    /**
     *
     * @param num1
     * @param num2
     * @return
     */
    public static int devide(int num1, int num2) {
        return num1 / num2;
    }

}

